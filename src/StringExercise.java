public class StringExercise {
    public static void main(String[] args) {
        String display;
        display = "We don't need no education";
        System.out.println(display);

        display = "We don't need no thought control";
        System.out.println(display);

        display = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(display);

        display = "In windows, the main drive is usually C:\\";
        System.out.println(display);

        display = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\";
        System.out.println(display);
    }
}
